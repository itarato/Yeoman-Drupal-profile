'use strict';

var generators = require('yeoman-generator');

module.exports = generators.Base.extend({

  constructor: function () {
    generators.Base.apply(this, arguments);
  },

  prompting: function () {
    this.log('Prompting.');

    var asyncWait = this.async();

    var questions = [{
      name: 'profileName',
      message: 'Profile full name'
    }, {
      name: 'machineName',
      message: 'Machine name'
    }, {
      type: 'list',
      name: 'drupalCore',
      message: 'Drupal core version',
      choices: ['Drupal 6', 'Drupal 7', 'Drupal 8'],
      default: 'Drupal 7'
    }, {
      name: 'profileDescription',
      message: 'Description',
      default: 'Auto-generated by "yo drupal-profile"'
    }, {
      type: 'confirm',
      name: 'inheritBasicProfile',
      message: 'Inherit from "Basic profile"',
      default: false
    }, {
      name: 'adminTheme',
      message: 'Admin theme',
      default: 'adminimal_theme'
    }, {
      type: 'checkbox',
      name: 'modules',
      message: 'Modules',
      choices: ['devel', 'views', 'ctools', 'entity', 'admin_menu']
    }];

    this.prompt(questions, function ( answers ) {
      this.profileName = answers.profileName;

      asyncWait();
    }.bind(this));
  },

  generateFolder: function () {
    this.log('Folder generation.');

    // @todo to machine name
    this.mkdir(this.profileName);
  }

});
